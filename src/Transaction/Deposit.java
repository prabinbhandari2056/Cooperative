    private void jProceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jProceedActionPerformed
        //To deposit
        jCheckBoxdeposit.setActionCommand("Deposit");
        Integer Member_id=Integer.parseInt(jTextFieldMemberId.getText());
        String Account_name=jTextFieldAccountName.getText();
        Integer Account_no=Integer.parseInt(jTextFieldAccountNumber.getText());
        int Deposit_amount=Integer.parseInt(jTextFieldAmount.getText());
        String Deposit_Depositedby=jTextFieldDepositedBy.getText();
        String Deposit_Remarks=jTextFieldRemarks.getText();
        String Transaction_type=buttonGroup1.getSelection().getActionCommand();
        Connection con=null;
        con=ConnectionProvider.getCon();
        ResultSet rs = null;
        try{
            con.setAutoCommit(false);
            //step3: create sql query and execute it
            String sql="insert into Transaction (Member_id,Account_no,Account_name,"
                    + "Transaction_type,Amount,Transacted_by,Remarks)values(?,?,?,?,?,?,?)";
            PreparedStatement st=con.prepareStatement(sql);
            st.setInt(1, Member_id);
             st.setInt(2, Account_no);
            st.setString(3, Account_name);
            st.setString(4, Transaction_type);
            st.setInt(5,Deposit_amount);
            st.setString(6, "Amount deposited by "+Deposit_Depositedby);        
            st.setString(7, Deposit_Remarks);
            st.execute();
            String sql2="select Balance from account where Account_no="+Account_no;
            double balance=0;
            PreparedStatement st2=con.prepareStatement(sql2);
            rs=st2.executeQuery();
            while(rs.next()){
                balance=rs.getDouble("Balance");
            }
            balance=balance+Deposit_amount;
            st2.execute();
            String sql3="Update account set Balance=? where Account_no=?";
            PreparedStatement st3=con.prepareStatement(sql3);
            st3.setDouble(1, balance);
            st3.setInt(2, Account_no);
            st3.execute();
            //step4: close the connection
            con.commit();
            con.close(); 
        JOptionPane.showMessageDialog(null,"Amount"+ Deposit_amount+" has been deposited in account  "+Account_no);
        }
        catch(Exception ex){
            try{
                con.rollback();
            }catch(Exception e){
                System.out.println(e);
            }
            System.out.println(ex);
        }
        try {
            jTextFieldAccountName.setText(null);
            jTextFieldAccountNumber.setText(null);
            jTextFieldAmount.setText(null);
            jTextFieldDepositedBy.setText(null);
            jTextFieldRemarks.setText(null);
            jTextFieldMemberId.setText(null);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jProceedActionPerformed

