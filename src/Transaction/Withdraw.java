    private void jTextFieldAccountNoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldAccountNoKeyPressed
        //To display account name and memberid when account no. entered
              try {
             char c=evt.getKeyChar();
        if (Character.isDigit(c)|| evt.getKeyCode()==KeyEvent.VK_BACK_SPACE ||evt.getKeyCode()==KeyEvent.VK_DELETE||evt.getKeyCode() == KeyEvent.VK_ENTER || evt.getKeyCode() == KeyEvent.VK_DOWN || evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            jTextFieldAccountNo.setEditable(true);
        }
        else{
            jTextFieldAccountNo.setEditable(false);
        }
        } catch (Exception e) {
        }
        
         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        Integer Account_no=Integer.parseInt(jTextFieldAccountNo.getText());
        Connection con= ConnectionProvider.getCon();
        ResultSet rs = null;
           try{
           
           //step3: create sql query and execute it
          String sql="select Member_id,Account_name from account where Account_no="+Account_no;
           PreparedStatement st=con.prepareStatement(sql);
           rs=st.executeQuery();
           while(rs.next()){
               int Member_id=rs.getInt("Member_id");
               String Account_name=rs.getString("Account_name");
               jTextFieldAccountName.setText(Account_name);
               jTextFieldMemberID.setText(String.valueOf(Member_id));
               
           }
          //step4: close the connection
           con.close();
           }
       catch(Exception ex){
            System.out.println(ex);
}
           jTextFieldAmount.requestFocus();
    }  
    }//GEN-LAST:event_jTextFieldAccountNoKeyPressed

