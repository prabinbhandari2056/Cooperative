        String Supplier_Address=jTextFieldSupplieraddress.getText();
        String Supplier_Contactno=jTextFieldContactno.getText();
        String Supplier_Email=jTextFieldEmail.getText();
        Connection con= ConnectionProvider.getCon();
           try{
           
           //step3: create sql query and execute it
           String sql="update Supplier set Supplier_Name=?,Supplier_Address=?,Supplier_Contactno=?,Supplier_Email=? where Supplier_id=?";
           PreparedStatement st=con.prepareStatement(sql);
           st.setString(1, Supplier_name);
           st.setString(2,Supplier_Address);
           st.setString(3, Supplier_Contactno);
           st.setString(4,Supplier_Email);
           st.setInt(5, Supplier_id);
           st.executeUpdate();
           
          //step4: close the connection
           con.close();
           JOptionPane.showMessageDialog(rootPane, "Updated Supplier with supplier id"+Supplier_id);

           }
       catch(Exception ex){
            System.out.println(ex);
}
