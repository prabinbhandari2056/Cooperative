    private void jTextFieldFullNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldFullNameKeyPressed
        // TO Transfer focus to another textfield
       char c=evt.getKeyChar();
        try {
            if (Character.isLetter(c)||evt.getKeyCode()==KeyEvent.VK_ENTER||
                    evt.getKeyCode()==KeyEvent.VK_BACK_SPACE||
                    evt.getKeyCode()==KeyEvent.VK_DELETE||
                    evt.getKeyCode()==KeyEvent.VK_LEFT||
                    evt.getKeyCode()==KeyEvent.VK_RIGHT||
                    evt.getKeyCode()==KeyEvent.VK_BACK_SPACE||
                    evt.getKeyCode()==KeyEvent.VK_DELETE||
                    evt.getKeyCode()==KeyEvent.VK_LEFT||
                    evt.getKeyCode()==KeyEvent.VK_UP||evt.getKeyCode()==KeyEvent.VK_SPACE) {
            jTextFieldFullName.setEditable(true);
        }
        else{
            jTextFieldFullName.setEditable(false);
        }
        } catch (Exception e) {
        }
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            jRadioButtonMale.requestFocus();
        }
        else if (evt.getKeyCode()==KeyEvent.VK_RIGHT) {
             jRadioButtonMale.requestFocus();
        }
        else if (evt.getKeyCode()==KeyEvent.VK_DOWN) {
            jTextFieldContactNo.requestFocus();
        }
    }//GEN-LAST:event_jTextFieldFullNameKeyPressed

