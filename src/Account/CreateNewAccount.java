    private void jTextFieldMemberidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldMemberidKeyPressed
        // TODO add your handling code here:
         char c=evt.getKeyChar();
        try {
            if (Character.isDigit(c)||evt.getKeyCode()==KeyEvent.VK_ENTER||evt.getKeyCode()==KeyEvent.VK_BACK_SPACE||evt.getKeyCode()==KeyEvent.VK_DELETE||evt.getKeyCode()==KeyEvent.VK_LEFT||evt.getKeyCode()==KeyEvent.VK_RIGHT||evt.getKeyCode()==KeyEvent.VK_BACK_SPACE||evt.getKeyCode()==KeyEvent.VK_DELETE||evt.getKeyCode()==KeyEvent.VK_LEFT||evt.getKeyCode()==KeyEvent.VK_UP) {
            jTextFieldMemberid.setEditable(true);
        }
        else{
            jTextFieldMemberid.setEditable(false);
        }
        } catch (Exception e) {
        }
        if (evt.getKeyCode()==KeyEvent.VK_ENTER||evt.getKeyCode()==KeyEvent.VK_DOWN||evt.getKeyCode()==KeyEvent.VK_LEFT||evt.getKeyCode()==KeyEvent.VK_RIGHT) {
            jTextFieldAccountName.requestFocus();
        }
    }//GEN-LAST:event_jTextFieldMemberidKeyPressed

    private void jTextFieldAccountNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldAccountNameKeyPressed
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        try {
            if (Character.isLetter(c)||evt.getKeyCode()==KeyEvent.VK_ENTER||
                    evt.getKeyCode()==KeyEvent.VK_BACK_SPACE||evt.getKeyCode()==KeyEvent.VK_DELETE||
                    evt.getKeyCode()==KeyEvent.VK_LEFT||
                    evt.getKeyCode()==KeyEvent.VK_RIGHT||evt.getKeyCode()==KeyEvent.VK_BACK_SPACE||
                    evt.getKeyCode()==KeyEvent.VK_DELETE||evt.getKeyCode()==KeyEvent.VK_LEFT||
                    evt.getKeyCode()==KeyEvent.VK_UP||evt.getKeyCode()==KeyEvent.VK_SPACE) {
            jTextFieldAccountName.setEditable(true);
        }
        else{
            jTextFieldAccountName.setEditable(false);
        }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTextFieldAccountNameKeyPressed

