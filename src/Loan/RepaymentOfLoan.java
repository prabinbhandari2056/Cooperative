    private void jTextFieldLoanidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldLoanidKeyPressed
        // TODO add your handling code here:
         try {
            char c=evt.getKeyChar();
        if (Character.isDigit(c)|| evt.getKeyCode()==KeyEvent.VK_BACK_SPACE ||evt.getKeyCode()==KeyEvent.VK_DELETE||evt.getKeyCode() == KeyEvent.VK_ENTER || evt.getKeyCode() == KeyEvent.VK_DOWN || evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            jTextFieldLoanid.setEditable(true);
        }
        else{
            jTextFieldLoanid.setEditable(false);
        }
        } catch (Exception e) {
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER || 
                evt.getKeyCode() == KeyEvent.VK_DOWN || 
                evt.getKeyCode() == KeyEvent.VK_RIGHT) {
        Integer Loan_id=Integer.parseInt(jTextFieldLoanid.getText());
        Double Remaining_LoanAmount1=0.0;
        Double Pmt=0.0;
       
        ResultSet rs = null;
        ResultSet rs1 = null;
             Connection con= ConnectionProvider.getCon();

           try{
          
           con.setAutoCommit(false);

           //step3: create sql query and execute it
           String sql="select * from loan where Loan_id=?";
           PreparedStatement st=con.prepareStatement(sql);
           st.setInt(1, Loan_id);
           rs=st.executeQuery();
           String s1=String.valueOf(rs);
               if(rs.wasNull()) {
                   System.err.println("Invalid Loan Id");
               }

               else{
               while(rs.next()){
               int Member_id=rs.getInt("Member_id");
               String Member_name=rs.getString("Member_name");
               jTextFieldMembername.setText(Member_name);
               jTextFieldMemberid.setText(String.valueOf(Member_id));
               }
               }
               //RemainingLoan
           
          //step4: close the connection
          
           String sql1="select * from remaining_loan where loan_id=?";
           PreparedStatement st1=con.prepareStatement(sql1);
           st1.setInt(1, Loan_id);
           rs1=st1.executeQuery();
               while (rs1.next()) {                   
                   Remaining_LoanAmount1=rs1.getDouble("Remaining_LoanAmount");
                   Pmt=rs1.getDouble("Loan_pmt");
                   jTextFieldRemainingloan.setText(String.valueOf(Remaining_LoanAmount1));
                   jTextFieldAmountPayable.setText(String.valueOf(Pmt));
               }
               Double Interest=Remaining_LoanAmount1*0.13;
               String Interest1=String.format("%.4f", Interest);
               jTextFieldInterest.setText(Interest1);
                con.commit();
           con.close();
           }
       catch(Exception ex){
           try{
              con.rollback();
           }catch(Exception e){
               JOptionPane.showMessageDialog(rootPane,"Invalid Account no.");
           }
       }
            try {
                if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                    jTextFieldAmountPayable.requestFocus();
                }
            } catch (Exception e) {
            }
            
           
      }
    }//GEN-LAST:event_jTextFieldLoanidKeyPressed

